<template>
  <p> you have visited {{totalVisited}} {{stateUnits}}</p>

  <p id="all-visited-message" v-if="areAllVisited">You have visited all the states & DC</p>
</template>

<script>
export default {
  name: "StateSummary",
  props: {
    states: Array
  },
  computed: {
    totalVisited() {
      let visitedCount = 0
      this.states.forEach( state => {
        if (state.visited) {
          visitedCount++
        }
      })
      return visitedCount
    },
    stateUnits(){
      if (this.totalVisited == 1){
        return 'state'
      } else {
        return 'states'
      }
    },
    areAllVisited() {
      return this.totalVisited === this.states.length
    }
  }
}
</script>

<style scoped>

#all-visited-message {
  color: azure;
  font-weight: bold;
}

</style>